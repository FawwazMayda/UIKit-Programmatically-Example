import Foundation

class Character {
    var name: String
    var army: String
    var gender: String
    var imgName: String
    
    init(name: String, army: String, gender: String, imgName: String) {
        self.name = name
        self.army = army
        self.gender = gender
        self.imgName = imgName
    }
}
